#!/usr/bin/env node
// Emit studio/world-graph-data.js from entries/ for the World Graph UI surface.
import { readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const codexRoot = join(dirname(fileURLToPath(import.meta.url)), '..');
const entriesDir = join(codexRoot, 'entries');
const outPath = join(codexRoot, 'studio', 'world-graph-data.js');

const entries = readdirSync(entriesDir)
  .filter((f) => f.endsWith('.json'))
  .map((f) => JSON.parse(readFileSync(join(entriesDir, f), 'utf8')))
  .sort((a, b) => a.title.localeCompare(b.title));

const stats = {
  entries: entries.length,
  open_packets: entries.reduce((n, e) => n + (e.packets || []).filter((p) => p.status === 'open').length, 0),
  by_type: {},
  by_status: {},
};
for (const e of entries) {
  stats.by_type[e.type] = (stats.by_type[e.type] || 0) + 1;
  stats.by_status[e.status] = (stats.by_status[e.status] || 0) + 1;
}

const body =
  '// Generated by codex/tools/project-studio-world.mjs — do not edit by hand.\n' +
  'window.CODEX_WORLD_GRAPH = ' + JSON.stringify(entries, null, 2) + ';\n' +
  'window.CODEX_WORLD_STATS = ' + JSON.stringify(stats, null, 2) + ';\n';

writeFileSync(outPath, body);
console.log('Studio world graph projection');
console.log('  entries       :', stats.entries, '· open packets', stats.open_packets);
console.log('  by type       :', stats.by_type);
console.log('  wrote         :', outPath);